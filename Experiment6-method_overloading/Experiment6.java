class Experiment6
{
	public static void main(String[] args)
	{
		String s1 = new String("Vihaan ");
		String s2 = s1.concat("Sharma");
		String s3 = s2.toLowerCase();
		String s4 = s3.toUpperCase();
		String s5 = s4.substring(2);
		System.out.println(" ");
		System.out.println("Original String");
		System.out.println("------------------------------\n"+s1);
		System.out.println(" ");
		System.out.println("Concatenated String");
		System.out.println("------------------------------\n"+s2);
		System.out.println(" ");
		System.out.println("LowerCase String");
		System.out.println("------------------------------\n"+s3);
		System.out.println(" ");
		System.out.println("UpperCase String");
		System.out.println("------------------------------\n"+s4);
		System.out.println(" ");
		System.out.println("Substring String");
		System.out.println("------------------------------\n"+s5);
	}
}
		
		
		
		
		